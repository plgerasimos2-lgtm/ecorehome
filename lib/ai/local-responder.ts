const DEFAULT_ANSWER =
  "Δεν είμαι σίγουρος για αυτή την ερώτηση. Η ομάδα της Eco ReHome μπορεί να σας βοηθήσει άμεσα.";

function normalize(text: string): string {
  return text
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[.,!?;:()«»""]/g, " ");
}

export function getLocalChatReply(userMessage: string): string {
  const normalized = normalize(userMessage);

  let bestScore = 0;
  let bestAnswer = DEFAULT_ANSWER;

  for (const entry of KNOWLEDGE) {
    let score = 0;
    for (const keyword of entry.keywords) {
      if (normalized.includes(normalize(keyword))) {
        score += 1;
      }
    }
    if (score > bestScore) {
      bestScore = score;
      bestAnswer = entry.answer;
    }
  }

  if (bestScore === 0) {
    return `${DEFAULT_ANSWER} ${CONTACT_FOOTER}`;
  }

  return `${bestAnswer} ${CONTACT_FOOTER}`;
}
